// ===== REVERSE - একটি বাক্য উল্টো করে লেখা =====
// রিয়েল লাইফ: যেমন একটি মেসেজ "I am a web developer" আপনি পড়লেন,
// কিন্তু এটি উল্টো করে লিখলে "repoleved bew a ma I" হয়ে যাবে!

const sentence = 'I am a web developer';

// ===== METHOD: FOR LOOP দিয়ে উল্টো করা =====
// ধারণা: প্রতিটি অক্ষরকে সামনে রাখতে থাকুন (যেমন আয়না তে উল্টো দেখা যায়)
let reverse = ' ';  // শুরুতে একটি খালি স্পেস

for (const letter of sentence) {  // প্রতিটি অক্ষর এক এক করে নিন
    reverse = letter + reverse;    // নতুন অক্ষর সামনে রাখুন, পুরাতনটা পেছনে
}
// উদাহরণ:
// 1ম লুপ: 'I' + ' ' = 'I '
// 2য় লুপ: ' ' + 'I ' = ' I '
// 3য় লুপ: 'a' + ' I ' = 'a I '
// ... এভাবে চলতে থাকে

console.log(reverse);  // output: "repoleved bew a ma I"


// ===== BONUS: প্রতিটি অক্ষর একটি একটি প্রিন্ট করা =====
// রিয়েল লাইফ: একটি শব্দের প্রতিটি অক্ষর আলাদাভাবে লেখা
for (let i = 0; i < sentence.length; i++) {  // 0 থেকে শুরু করে প্রতিটি অক্ষর
    // console.log(i);  // ইনডেক্স নম্বর (0, 1, 2, 3...)
    console.log(sentence[i]);  // প্রতিটি অক্ষর আলাদাভাবে
}
// output:
// I
//  (স্পেস)
// a
// m
// ...এবং এভাবে চলতে থাকে
